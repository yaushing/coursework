data = text.split("\n")
final = []
for row in data:
    final.append(list(map(int, list(row))))
for y in range(len(final)):
    for x in range(len(final[y])):
        if final[y][x] == 5:
            print(f"[15, {y - 1}, {x - 1}]", end = ", ")
        if final[y][x] == 4:
            print(f"[41, {y - 1}, {x - 1}]", end = ",")